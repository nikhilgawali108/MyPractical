// java program to find even or odd no.
class Number
{
	public static void main (String args[]) //main method
	{
		int a=17;    //data members
		if (a%2==0)   //if condition
		{
			System.out.println("the number is even");
		}
		
		else  //else condition
		{
			System.out.println("the number is odd");
		}
	}
}